export function generateDuckDBSQL(steps: TransformOperation[], filePath?: string): string {
  const lines: string[] = [
    '-- Generated by QuackWrangler',
    '-- Engine: DuckDB',
    '',
  ];

  if (steps.length === 0) lines.push('-- No transforms applied', '');

  if (filePath) {
    lines.push(`-- Source: ${filePath}`);
    lines.push('');
  }

  lines.push(buildPipelineSQL(steps));
  lines.push(';');

  return lines.join('\n');
}

export function generateDuckDBPython(steps: TransformOperation[], filePath?: string): string {
  const lines: string[] = [
    '# Generated by QuackWrangler',
    '# Engine: DuckDB',
    '',
    'import duckdb',
    '',
    'con = duckdb.connect()',
    '',
  ];

  if (filePath) {
    lines.push(`# Source: ${filePath}`);
    if (path.extname(filePath).toLowerCase() === '.parquet') {
      lines.push(`df = con.read_parquet(${pythonString(filePath)})`);
      lines.push('con.register("current_data", df)');
    } else {
      lines.push(`source_path = ${pythonString(filePath)}`);
      lines.push(`con.execute("CREATE TABLE current_data AS SELECT * FROM ${duckdbReader(filePath)}", [source_path])`);
    }
    lines.push('');
  }

  const pipeline = buildPipelineSQL(steps).replace(/'''/g, "\\'\\'\\'");
  steps.forEach((step, index) => lines.push(`# Step ${index + 1}: ${step.description}`));
  lines.push(`pipeline_sql = r'''${pipeline}'''`);
  lines.push('result = con.execute(pipeline_sql).fetchall()');
  lines.push('print(result)');

  return lines.join('\n');
}

