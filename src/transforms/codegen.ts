import * as path from 'path';

import { TransformOperation, EngineType } from '../types/index.js';

import { buildPipelineSQL } from './pipeline.js';

function pythonString(value: string): string {
  return `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

function pythonValue(value: unknown): string {
  const text = String(value ?? '').trim();
  if (/^-?(?:\d+\.?\d*|\.\d+)$/.test(text)) return text;
  if (/^true$/i.test(text)) return 'True';
  if (/^false$/i.test(text)) return 'False';
  if (/^null$/i.test(text)) return 'None';
  return pythonString(text);
}

function stringList(value: unknown, fallback?: unknown): string[] {
  const source = value ?? fallback;
  if (Array.isArray(source)) return source.filter((item): item is string => typeof item === 'string' && item.length > 0);
  return typeof source === 'string' && source.length > 0 ? [source] : [];
}

function polarsFilter(params: Record<string, unknown>): string {
  if (typeof params.condition === 'string' && params.condition.trim()) {
    return `pl.sql_expr(${pythonString(params.condition)})`;
  }
  const column = pythonString(String(params.column));
  const operator = String(params.operator);
  const value = pythonValue(params.value);
  const stringColumn = `pl.col(${column}).cast(pl.String)`;
  switch (operator) {
    case 'equals': return `pl.col(${column}) == ${value}`;
    case 'not_equals': return `pl.col(${column}) != ${value}`;
    case 'greater_than': return `pl.col(${column}) > ${value}`;
    case 'greater_equals': return `pl.col(${column}) >= ${value}`;
    case 'less_than': return `pl.col(${column}) < ${value}`;
    case 'less_equals': return `pl.col(${column}) <= ${value}`;
    case 'contains': return `${stringColumn}.str.contains(${pythonString(String(params.value ?? ''))}, literal=True)`;
    case 'not_contains': return `~${stringColumn}.str.contains(${pythonString(String(params.value ?? ''))}, literal=True)`;
    case 'starts_with': return `${stringColumn}.str.starts_with(${pythonString(String(params.value ?? ''))})`;
    case 'ends_with': return `${stringColumn}.str.ends_with(${pythonString(String(params.value ?? ''))})`;
    case 'is_null': return `pl.col(${column}).is_null()`;
    case 'is_not_null': return `pl.col(${column}).is_not_null()`;
    case 'between': return `pl.col(${column}).is_between(${value}, ${pythonValue(params.value2)})`;
    case 'in':
    case 'not_in': {
      const values = String(params.value ?? '').split(',').map(item => pythonValue(item.trim())).filter(Boolean);
      const expression = `pl.col(${column}).is_in([${values.join(', ')}])`;
      return operator === 'not_in' ? `~${expression}` : expression;
    }
    default: return `pl.lit(True)  # Unsupported filter operator: ${operator}`;
  }
}

function polarsReader(filePath: string): string {
  switch (path.extname(filePath).toLowerCase()) {
    case '.csv': return `pl.read_csv(${pythonString(filePath)})`;
    case '.tsv': return `pl.read_csv(${pythonString(filePath)}, separator="\\t")`;
    case '.jsonl': return `pl.read_ndjson(${pythonString(filePath)})`;
    case '.json': return `pl.read_json(${pythonString(filePath)})`;
    default: return `pl.read_parquet(${pythonString(filePath)})`;
  }
}

function duckdbReader(filePath: string): string {
  switch (path.extname(filePath).toLowerCase()) {
    case '.csv':
    case '.tsv': return 'read_csv_auto(?)';
    case '.json':
    case '.jsonl': return 'read_json_auto(?)';
    default: return 'read_parquet(?)';
  }
}

